import org.springframework.beans.factory.annotation.Value;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

import javax.servlet.http.Cookie;
import javax.servlet.http.HttpServletResponse;
import java.util.Map;

@RestController
public class AuthController extends BaseController implements AuthControllerApi {


    @Autowired
    AuthService authService;


    @PostMapping("/userlogin")
    public LoginResult login(LoginRequest loginRequest, HttpServletResponse response) {
        LoginResult login = authService.login(loginRequest, response);
        return login;
    }

    private void saveCookie(String access_token) {
    }

    @Override
    @PostMapping("/userlogout")
    public ResponseResult logout() {
        //取出身份令牌
        String uid = getTokenFormCookie();
        //删除redis中token
        authService.delToken(uid);
        //清除cookie
        authService.clearCookie(uid,response);
        return new ResponseResult(CommonCode.SUCCESS);
    }


    @GetMapping("/userjwt")
    public JwtResult userjwt() {
        String access_token = getTokenFormCookie();
        //根据令牌从redis查询jwt
        AuthToken authToken = authService.getUserToken(access_token);
        if(authToken == null){
            return new JwtResult(CommonCode.FAIL,null);
        }
        return new JwtResult(CommonCode.SUCCESS,authToken.getJwt_token());
    }
    //从cookie中读取访问令牌
    private String getTokenFormCookie(){
        Map<String, String> cookieMap = CookieUtil.readCookie(request, "uid");
        String access_token = cookieMap.get("uid");
        return access_token;
    }
}
